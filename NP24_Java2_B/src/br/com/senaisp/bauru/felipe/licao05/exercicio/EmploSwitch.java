package br.com.senaisp.bauru.felipe.licao05.exercicio;

import java.util.Scanner;

public class EmploSwitch {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Digite um valor de 1 a 10:");
		int vlr = sc.nextInt();
		switch (vlr) {
		case 1: System.out.println("Você ganhou um boné");
		case 2: System.out.println("Você ganhou um canete");
		case 3: System.out.println("Você ganhou um lápis");
		case 4: System.out.println("Você ganhou um calculadora");
		case 5: System.out.println("Você ganhou um borracha");
		case 6: System.out.println("Você ganhou um régua"); break;
		case 7: System.out.println("Você ganhou um transferidor");
		case 8: System.out.println("Você ganhou um compasso");
		case 9: System.out.println("Você ganhou um celula");
		case 10: System.out.println("Você ganhou um 1.000");
		default: System.out.println("Que pena você perdeu tudo!");
		
		}
		sc.close();
	}

}
